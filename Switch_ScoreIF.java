public class Switch_ScoreIF{
	public static void main (String args[]){

		int score=85;
		if(score>=90){
				System.out.println("A입니다.");
		}
		else if(score>=80){
				System.out.println("B입니다.");
		}
		else if(score>=70){					//기호주의, 오타주의
				System.out.println("C입니다.");
		}
		else{
				System.out.println("D입니다.");	
		}		
	}
}