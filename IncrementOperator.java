public class IncrementOperator{
	public static void main(String args[]){

		//증감연산자(증가/감소 연산자)
		int a=10;
		System.out.println(a++);	//a를 출력한 후 1을 더한다. 결괏값 : "10"
		System.out.println(a);	//결괏값 : "11"

		System.out.println(++a);	//a에 1을 더한 후 출력한다. 결괏값 : "12"
		System.out.println(a);	//결괏값 : "12"
	}
}

